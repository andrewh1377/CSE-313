	DISPLAY2 .STRINGZ "\nenter the 2nd number: "
	STRINGRESULT1 .STRINGZ "\nResult for X + Y: "
	STRINGRESULT2 .STRINGZ "\nResult for X AND Y: "
	STRINGRESULT3 .STRINGZ "\nResult for X OR Y: "
	STRINGRESULT4 .STRINGZ "\nResult for NOT(X): "
	STRINGRESULT5 .STRINGZ "\nResult for NOT(Y): "
	STRINGRESULT6 .STRINGZ "\nResult for X + 3: "
	STRINGRESULT7 .STRINGZ "\nResult for Y - 3: "
	


.END
	

	
